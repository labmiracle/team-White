CREATE OR REPLACE VIEW `roleView`
AS
SELECT
     r.`id`
    ,r.`name`
    ,r.`description`
FROM `role` r;

CREATE OR REPLACE VIEW `userView`
AS
SELECT
     u.`id`
    ,u.`companyId`
    ,c.`name`                     AS `companyName`
    ,u.roleId
    ,r.name                       AS `roleName`
    ,u.`userName`
    ,u.`firstName`
    ,u.`lastName`
    ,u.`email`
    ,u.`registrationDate`
    ,CAST(NULL as CHAR(1))        AS `passwordHash`
FROM `user` u
INNER JOIN `company` c
        ON u.companyId = c.id
INNER JOIN `role` r
        ON u.roleId = r.id;

